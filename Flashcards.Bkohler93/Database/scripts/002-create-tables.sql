CREATE TABLE Stacks(
    Id INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
    Name VARCHAR(255)
);

CREATE TABLE Flashcards (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    StackId INT NOT NULL,
    Front VARCHAR(255),
    Back VARCHAR(255),
    FOREIGN KEY (StackId) REFERENCES Stacks(Id) ON DELETE CASCADE
);

CREATE TABLE StudySessions (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    StackId INT NOT NULL,
    StudyTime DATETIME,
    Score INT,
    FOREIGN KEY (StackId) REFERENCES Stacks(Id) ON DELETE CASCADE
);